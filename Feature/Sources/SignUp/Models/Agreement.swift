enum AgreementType: String, Identifiable {
    case service = "서비스 이용약관"
    case privacy = "개인정보 수집 및 이용"

    var id: String { self.rawValue }

    var content: String {
        switch self {
        case .service:
            return """
            서비스 이용약관

            제1조 (목적)
            이 약관은 FRENTE(이하 \"회사\")가 제공하는 수원삼성 팬들을 위한 온라인 중고거래 중개 플랫폼(이하 \"서비스\")의 이용과 관련하여 회사와 회원 간의 권리, 의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 합니다.

